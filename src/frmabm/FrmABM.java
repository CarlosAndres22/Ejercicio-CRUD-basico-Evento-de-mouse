    private void GrillaMouseClicked(java.awt.event.MouseEvent evt) {                                    
        //En este evento pasamos los datos a las cajas de texto cuando se dan click sobre ella
        int Fila = Grilla.rowAtPoint(evt.getPoint());
        LblID.setText(Grilla.getValueAt(Fila, 0).toString());
        TBNombre.setText(Grilla.getValueAt(Fila, 1).toString());
        TBDni.setText(Grilla.getValueAt(Fila, 2).toString());
       
        
        BtnEditar.setEnabled(true);
        BtnEliminar.setEnabled(true);
    }                                   

    private void BtnEditarActionPerformed(java.awt.event.ActionEvent evt) {                                          
        FAbm Objeto = new FAbm();
        int ID = Integer.parseInt(LblID.getText());
        String Nombre = TBNombre.getText();
        int Dni = Integer.parseInt(TBDni.getText());
        Objeto.Editar(ID, Nombre, Dni);
        JOptionPane.showConfirmDialog(rootPane, "Persona guardada con exito");
        LlenarGrilla();
    }                                         

    private void BtnEliminarActionPerformed(java.awt.event.ActionEvent evt) {                                            
        FAbm Objeto = new FAbm();
        int ID = Integer.parseInt(LblID.getText());
        Objeto.Eliminar(ID);
        JOptionPane.showConfirmDialog(rootPane, "Persona eliminada con exito");
        LlenarGrilla();
        TBNombre.setText("");
        TBDni.setText("");
        LblID.setText("");
        BtnEditar.setEnabled(false);
        BtnEliminar.setEnabled(false);
    }                                           

