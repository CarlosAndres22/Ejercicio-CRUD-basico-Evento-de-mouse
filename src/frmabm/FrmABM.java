    private void BtnAgregarActionPerformed(java.awt.event.ActionEvent evt) {                                           
        if (TBNombre.getText().length() > 0 && TBDni.getText().length() > 0) {
            FAbm Objeto = new FAbm();
            String Nombre = TBNombre.getText();
            int Dni = Integer.parseInt(TBDni.getText());
            Objeto.InsertarPersonas(Nombre, Dni);
            JOptionPane.showConfirmDialog(rootPane, "Persona guardada con exito");
            LlenarGrilla();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Debe completar los campos");
        }
    }                                          

